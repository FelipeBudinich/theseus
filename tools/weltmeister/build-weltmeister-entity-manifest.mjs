import { glob } from 'node:fs/promises';
import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const DEFAULT_SOURCE_DIRECTORIES = ['public/games/example/entities'];
const DEFAULT_MODULE_OUTPUT_PATH = 'tools/weltmeister/entity-manifest.js';
const DEFAULT_JSON_OUTPUT_PATH = 'tools/weltmeister/entity-manifest.json';

const toPosixPath = (value) => value.split(path.sep).join('/');

const normalizeRelativePath = (value) =>
  toPosixPath(String(value ?? ''))
    .replace(/^\.\//, '')
    .replace(/\/+/g, '/')
    .replace(/\/$/, '');

const toClassName = (filePath) => {
  const fileName = path.posix.basename(filePath, '.js');
  return `Entity${fileName.replace(/(^|-)(\w)/g, (_match, _prefix, character) =>
    character.toUpperCase()
  )}`;
};

const toDisplayName = (className) => className.replace(/^Entity/, '');

const toPublicPath = (filePath) => toPosixPath(filePath).replace(/^public\//, '');

const toModuleId = (filePath) =>
  toPublicPath(filePath)
    .replace(/\.js$/, '')
    .replace(/\//g, '.');

const toStableKey = (filePath) =>
  toPublicPath(filePath).replace(/\.js$/, '');

const toImportPath = (filePath, moduleOutputPath) => {
  const publicPath = toPublicPath(filePath);
  const relativePath = path.posix.relative(path.posix.dirname(moduleOutputPath), publicPath);
  return relativePath.startsWith('.') ? relativePath : `./${relativePath}`;
};

const uniqueSorted = (values) =>
  Array.from(new Set(values.map((value) => normalizeRelativePath(value)).filter(Boolean))).sort(
    (left, right) => left.localeCompare(right)
  );

const scanEntitySourceFiles = async ({
  projectRoot,
  sourceDirectories = DEFAULT_SOURCE_DIRECTORIES
}) => {
  const files = [];

  for (const directory of uniqueSorted(sourceDirectories)) {
    for await (const filePath of glob(`${directory}/**/*.js`, { cwd: projectRoot })) {
      files.push(toPosixPath(filePath));
    }
  }

  return uniqueSorted(files);
};

const buildEntityManifestEntries = ({
  sourceFiles,
  moduleOutputPath = DEFAULT_MODULE_OUTPUT_PATH
}) =>
  uniqueSorted(sourceFiles).map((filePath) => {
    const className = toClassName(filePath);

    return {
      key: toStableKey(filePath),
      moduleId: toModuleId(filePath),
      className,
      displayName: toDisplayName(className),
      filePath: toPublicPath(filePath),
      importPath: toImportPath(filePath, normalizeRelativePath(moduleOutputPath))
    };
  });

const renderValue = (value, indentLevel = 0) => {
  if (Array.isArray(value)) {
    if (value.length === 0) {
      return '[]';
    }

    const indent = '  '.repeat(indentLevel);
    const innerIndent = '  '.repeat(indentLevel + 1);
    return `[\n${value
      .map((entry) => `${innerIndent}${renderValue(entry, indentLevel + 1)}`)
      .join(',\n')}\n${indent}]`;
  }

  if (value && typeof value === 'object') {
    const entries = Object.entries(value);
    if (entries.length === 0) {
      return '{}';
    }

    const indent = '  '.repeat(indentLevel);
    const innerIndent = '  '.repeat(indentLevel + 1);
    return `{\n${entries
      .map(([key, entryValue]) => `${innerIndent}${key}: ${renderValue(entryValue, indentLevel + 1)}`)
      .join(',\n')}\n${indent}}`;
  }

  return JSON.stringify(value);
};

const renderManifestModule = ({ sourceDirectories, entries }) => `// Generated by \`npm run build:weltmeister-entity-manifest\`. Do not edit by hand.
const entitySourceDirectories = Object.freeze(${renderValue(sourceDirectories)});

const entityManifest = Object.freeze(
  ${renderValue(entries).replace(/\n/g, '\n  ')}
);

const entityModuleMap = Object.freeze(
  Object.fromEntries(entityManifest.map((entry) => [entry.moduleId, entry.filePath]))
);

const entityImporters = Object.freeze(
  Object.fromEntries(entityManifest.map((entry) => [entry.key, () => import(entry.importPath)]))
);

const loadEntityManifestModules = async () =>
  Promise.all(entityManifest.map((entry) => entityImporters[entry.key]()));

export {
  entityImporters,
  entityManifest,
  entityModuleMap,
  entitySourceDirectories,
  loadEntityManifestModules
};

export default entityManifest;
`;

const renderManifestJson = ({ sourceDirectories, entries }) =>
  `${JSON.stringify(
    {
      sourceDirectories,
      entities: entries
    },
    null,
    2
  )}\n`;

const buildEntityManifestArtifacts = async ({
  projectRoot = process.cwd(),
  sourceDirectories = DEFAULT_SOURCE_DIRECTORIES,
  moduleOutputPath = DEFAULT_MODULE_OUTPUT_PATH,
  jsonOutputPath = DEFAULT_JSON_OUTPUT_PATH
} = {}) => {
  const normalizedSourceDirectories = uniqueSorted(sourceDirectories);
  const normalizedModuleOutputPath = normalizeRelativePath(moduleOutputPath);
  const normalizedJsonOutputPath = normalizeRelativePath(jsonOutputPath);
  const sourceFiles = await scanEntitySourceFiles({
    projectRoot,
    sourceDirectories: normalizedSourceDirectories
  });

  if (sourceFiles.length === 0) {
    throw new Error(
      `No entity source files found in: ${normalizedSourceDirectories.join(', ')}`
    );
  }

  const entries = buildEntityManifestEntries({
    sourceFiles,
    moduleOutputPath: normalizedModuleOutputPath
  });

  return {
    entries,
    jsonOutputPath: normalizedJsonOutputPath,
    jsonSource: renderManifestJson({
      sourceDirectories: normalizedSourceDirectories,
      entries
    }),
    moduleOutputPath: normalizedModuleOutputPath,
    moduleSource: renderManifestModule({
      sourceDirectories: normalizedSourceDirectories,
      entries
    }),
    sourceDirectories: normalizedSourceDirectories,
    sourceFiles
  };
};

const writeGeneratedFile = async (projectRoot, relativePath, contents) => {
  const outputPath = path.join(projectRoot, relativePath);
  await mkdir(path.dirname(outputPath), { recursive: true });
  await writeFile(outputPath, contents, 'utf8');
};

const writeEntityManifestArtifacts = async (options = {}) => {
  const artifacts = await buildEntityManifestArtifacts(options);
  const projectRoot = options.projectRoot ?? process.cwd();

  await Promise.all([
    writeGeneratedFile(projectRoot, artifacts.moduleOutputPath, artifacts.moduleSource),
    writeGeneratedFile(projectRoot, artifacts.jsonOutputPath, artifacts.jsonSource)
  ]);

  return artifacts;
};

const checkEntityManifestArtifacts = async (options = {}) => {
  const artifacts = await buildEntityManifestArtifacts(options);
  const projectRoot = options.projectRoot ?? process.cwd();

  const readCurrentFile = async (relativePath) => {
    try {
      return await readFile(path.join(projectRoot, relativePath), 'utf8');
    } catch (error) {
      if (error?.code === 'ENOENT') {
        return null;
      }

      throw error;
    }
  };

  const currentModuleSource = await readCurrentFile(artifacts.moduleOutputPath);
  const currentJsonSource = await readCurrentFile(artifacts.jsonOutputPath);

  return {
    ...artifacts,
    matches:
      currentModuleSource === artifacts.moduleSource && currentJsonSource === artifacts.jsonSource
  };
};

const parseCliArguments = (argv) => {
  const options = {
    jsonOutputPath: DEFAULT_JSON_OUTPUT_PATH,
    moduleOutputPath: DEFAULT_MODULE_OUTPUT_PATH,
    sourceDirectories: []
  };

  for (let index = 0; index < argv.length; index += 1) {
    const argument = argv[index];

    switch (argument) {
      case '--dir':
      case '--source-dir':
        options.sourceDirectories.push(argv[index + 1]);
        index += 1;
        break;
      case '--module-output':
        options.moduleOutputPath = argv[index + 1];
        index += 1;
        break;
      case '--json-output':
        options.jsonOutputPath = argv[index + 1];
        index += 1;
        break;
      case '--check':
        options.check = true;
        break;
      default:
        throw new Error(`Unknown argument: ${argument}`);
    }
  }

  if (options.sourceDirectories.length === 0) {
    options.sourceDirectories = DEFAULT_SOURCE_DIRECTORIES;
  }

  return options;
};

const runCli = async () => {
  const options = parseCliArguments(process.argv.slice(2));

  if (options.check) {
    const result = await checkEntityManifestArtifacts(options);

    if (!result.matches) {
      throw new Error(
        `Generated manifest is out of date. Run: npm run build:weltmeister-entity-manifest`
      );
    }

    return;
  }

  await writeEntityManifestArtifacts(options);
};

const isMainModule =
  process.argv[1] && fileURLToPath(import.meta.url) === path.resolve(process.argv[1]);

if (isMainModule) {
  runCli().catch((error) => {
    console.error(error.message);
    process.exitCode = 1;
  });
}

export {
  DEFAULT_JSON_OUTPUT_PATH,
  DEFAULT_MODULE_OUTPUT_PATH,
  DEFAULT_SOURCE_DIRECTORIES,
  buildEntityManifestArtifacts,
  buildEntityManifestEntries,
  checkEntityManifestArtifacts,
  parseCliArguments,
  renderManifestJson,
  renderManifestModule,
  scanEntitySourceFiles,
  writeEntityManifestArtifacts
};
